// Fácil 6. Faça uma função que recebe a idade de uma pessoa 
// em anos, meses e dias e retorna essa idade expressa em dias.


function conversaoDeIdade(ano, mes, dias){
    let totalDias
    return totalDias = dias +( mes * 30)+(ano * 365)
}

